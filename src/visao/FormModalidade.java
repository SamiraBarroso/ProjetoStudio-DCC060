    private void jTableModalidadeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableModalidadeMouseClicked
        String id = "" + jTableModalidade.getValueAt(jTableModalidade.getSelectedRow(), 0);
        conex.conexao();
        conex.executaSql("SELECT *FROM modalidade WHERE IdModalidade='" + id + "'");

        try {
            conex.rs.first();
            jTextFieldID.setText(Integer.toString(conex.rs.getInt("idModalidade")));
            jTextFieldValorMensal.setText(conex.rs.getString("valor"));
            jTextFieldDescricao.setText(conex.rs.getString("descricao"));
            jTextFieldObs.setText(conex.rs.getString("observacao"));

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao selecionar dados " + ex);
        }
        conex.desconecta();
        jButtonNovo.setEnabled(false);
        jButtonEditar.setEnabled(true);
        jButtonExcluir.setEnabled(true);
        jButtonCancelar.setEnabled(true);
        jButtonSalvar.setEnabled(false);

        jTextFieldID.setEnabled(false);
        jTextFieldDescricao.setEnabled(false);
        jTextFieldObs.setEnabled(false);
        jTextFieldValorMensal.setEnabled(false);
    }//GEN-LAST:event_jTableModalidadeMouseClicked

     public void preencherTabela(String sql) {
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"ID", "Descrição", "Observação", "Valor"};
        conex.conexao();
        conex.executaSql(sql);

        try {
            conex.rs.first();
            do {
                dados.add(new Object[]{conex.rs.getInt("idModalidade"), conex.rs.getString("descricao"), conex.rs.getString("observacao"),
                    conex.rs.getString("valor")});
            } while (conex.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Busque por outro Usuario.");
        }

        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        jTableModalidade.setModel(modelo);
        jTableModalidade.getColumnModel().getColumn(0).setPreferredWidth(60);
        jTableModalidade.getColumnModel().getColumn(0).setResizable(false);
        jTableModalidade.getColumnModel().getColumn(1).setPreferredWidth(240);
        jTableModalidade.getColumnModel().getColumn(1).setResizable(false);
        jTableModalidade.getColumnModel().getColumn(2).setPreferredWidth(180);
        jTableModalidade.getColumnModel().getColumn(2).setResizable(false);
        jTableModalidade.getColumnModel().getColumn(3).setPreferredWidth(150);
        jTableModalidade.getColumnModel().getColumn(3).setResizable(false);
        jTableModalidade.getTableHeader().setReorderingAllowed(false);
        jTableModalidade.setAutoResizeMode(jTableModalidade.AUTO_RESIZE_OFF);
        jTableModalidade.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        conex.desconecta();
    }
     
