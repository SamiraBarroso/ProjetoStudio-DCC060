    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if (flag == 1) {

            if (jTextFieldDescricao.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Preencha a Descricao para continuar");
                jTextFieldDescricao.requestFocus();
            } else if (jTextFieldValorMensal.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Preencha o Valor Mensalidade para continuar");
                jTextFieldValorMensal.requestFocus();
            } else if (jTextFieldID.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Preencha o ID para continuar");
                jTextFieldID.requestFocus();
            } else {
                mod.setIdModalidade(Integer.parseInt(jTextFieldID.getText()));
                mod.setDescrição(jTextFieldDescricao.getText());
                mod.setObservacao(jTextFieldObs.getText());
                mod.setValor(jTextFieldValorMensal.getText());

                control.salvar(mod);

                jTextFieldID.setText("");
                jTextFieldDescricao.setText("");
                jTextFieldObs.setText("");
                jTextFieldValorMensal.setText("");

                jTextFieldID.setEnabled(false);
                jTextFieldDescricao.setEnabled(false);
                jTextFieldObs.setEnabled(false);
                jTextFieldValorMensal.setEnabled(false);

                jButtonSalvar.setEnabled(false);
                jButtonCancelar.setEnabled(false);

                preencherTabela("SELECT * FROM modalidade ORDER BY idModalidade");
            }
        } else {
            mod.setIdModalidade(Integer.parseInt(jTextFieldID.getText()));
            mod.setDescrição(jTextFieldDescricao.getText());
            mod.setObservacao(jTextFieldObs.getText());
            mod.setValor(jTextFieldValorMensal.getText());

            control.editar(mod);

            jTextFieldID.setText("");
            jTextFieldDescricao.setText("");
            jTextFieldObs.setText("");
            jTextFieldValorMensal.setText("");

            jTextFieldID.setEnabled(false);
            jTextFieldDescricao.setEnabled(false);
            jTextFieldObs.setEnabled(false);
            jTextFieldValorMensal.setEnabled(false);

            jButtonNovo.setEnabled(true);
            jButtonSalvar.setEnabled(false);
            jButtonCancelar.setEnabled(false);

            preencherTabela("SELECT * FROM modalidade ORDER BY idModalidade");
        }

    }//GEN-LAST:event_jButtonSalvarActionPerformed

